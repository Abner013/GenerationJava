package OrientacaoObjeto;

public class Aviao {
	private String TipoAviao;		
	private String ModeloAviao;
	private double NummAviao;
	private int DonoAviao;
	
	
	public String getTipoAviao() {
		return TipoAviao;
	}
	public void setTipoAviao(String tipoAviao) {
		TipoAviao = tipoAviao;
	}
	public String getModeloAviao() {
		return ModeloAviao;
	}
	public void setModeloAviao(String modeloAviao) {
		ModeloAviao = modeloAviao;
	}
	public double getNummAviao() {
		return NummAviao;
	}
	public void setNummAviao(int nummAviao) {
		NummAviao = nummAviao;
	}
	public int getDonoAviao() {
		return DonoAviao;
	}
	public void setDonoAviao(int donoAviao) {
		DonoAviao = donoAviao;
	}
	
	
	
	
}
